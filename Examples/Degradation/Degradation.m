function model = Degradation()
	syms x
	syms k1 k2 k3
	model.sym.x = [x];
	model.sym.u = [];
	model.sym.p = [k1 k2 k3];
	model.sym.x0 = [0];
	model.sym.y = [x];
	model.sym.xdot = [k1-k2*x/(k3+x)];
end